package com.epcs.cliente.perfil.bean;

import java.io.Serializable;

/**
 * @author Jorge Vasquez
 *
 */
public class CriteriosBusquedaOferta implements Serializable{
	
	private static final long serialVersionUID = 1L;
	
	/*
	 * Campos referentes al cliente.
	 * Se obtienen en el controller
	 */
	private String movil;
	private int cfActualCliente;
	
	/*
	 * Campos referentes a la oferta. 
	 * Se obtienen desde el contenido configurado para la campana promocional del mes
	 */
	private String grupoCampana;
	private String tipoPlanBolsa;
	private String plantillaOferta;
	private String ordenCargoFijo;
	
	private boolean multiOferta;
	private boolean validarTipoPlanBolsa;
	private boolean validarCargoFijo;
	private boolean validarPlantillaOferta;
	

	/**
	 * @return the movil
	 */
	public String getMovil() {
		return movil;
	}
	/**
	 * @param movil the movil to set
	 */
	public void setMovil(String movil) {
		this.movil = movil;
	}
	/**
	 * @return the grupoCampana
	 */
	public String getGrupoCampana() {
		return grupoCampana;
	}
	/**
	 * @param grupoCampana the grupoCampana to set
	 */
	public void setGrupoCampana(String grupoCampana) {
		this.grupoCampana = grupoCampana;
	}
	/**
	 * @return the tipoPlanBolsa
	 */
	public String getTipoPlanBolsa() {
		return tipoPlanBolsa;
	}
	/**
	 * @param tipoPlanBolsa the tipoPlanBolsa to set
	 */
	public void setTipoPlanBolsa(String tipoPlanBolsa) {
		this.tipoPlanBolsa = tipoPlanBolsa;
	}
	/**
	 * @return the plantillaOferta
	 */
	public String getPlantillaOferta() {
		return plantillaOferta;
	}
	/**
	 * @param plantillaOferta the plantillaOferta to set
	 */
	public void setPlantillaOferta(String plantillaOferta) {
		this.plantillaOferta = plantillaOferta;
	}
	/**
	 * @return the cfActualCliente
	 */
	public int getCfActualCliente() {
		return cfActualCliente;
	}
	/**
	 * @param cfActualCliente the cfActualCliente to set
	 */
	public void setCfActualCliente(int cfActualCliente) {
		this.cfActualCliente = cfActualCliente;
	}
	/**
	 * @return the multiOferta
	 */
	public boolean isMultiOferta() {
		return multiOferta;
	}
	/**
	 * @param multiOferta the multiOferta to set
	 */
	public void setMultiOferta(boolean multiOferta) {
		this.multiOferta = multiOferta;
	}
	/**
	 * @return the ordenCargoFijo
	 */
	public String getOrdenCargoFijo() {
		return ordenCargoFijo;
	}
	/**
	 * @param ordenCargoFijo the ordenCargoFijo to set
	 */
	public void setOrdenCargoFijo(String ordenCargoFijo) {
		this.ordenCargoFijo = ordenCargoFijo;
	}
	/**
	 * @return the validarTipoPlanBolsa
	 */
	public boolean isValidarTipoPlanBolsa() {
		return validarTipoPlanBolsa;
	}
	/**
	 * @param validarTipoPlanBolsa the validarTipoPlanBolsa to set
	 */
	public void setValidarTipoPlanBolsa(boolean validarTipoPlanBolsa) {
		this.validarTipoPlanBolsa = validarTipoPlanBolsa;
	}
	/**
	 * @return the validarCargoFijo
	 */
	public boolean isValidarCargoFijo() {
		return validarCargoFijo;
	}
	/**
	 * @param validarCargoFijo the validarCargoFijo to set
	 */
	public void setValidarCargoFijo(boolean validarCargoFijo) {
		this.validarCargoFijo = validarCargoFijo;
	}
	/**
	 * @return the validarPlantillaOferta
	 */
	public boolean isValidarPlantillaOferta() {
		return validarPlantillaOferta;
	}
	/**
	 * @param validarPlantillaOferta the validarPlantillaOferta to set
	 */
	public void setValidarPlantillaOferta(boolean validarPlantillaOferta) {
		this.validarPlantillaOferta = validarPlantillaOferta;
	}
	
		
}
