package com.epcs.bean;

import java.io.Serializable;

/**
 * Bean con resultado de accion de administracion de servicio para
 * {@link ServicioBean}
 * 
 * @author jlopez (I2B) en nombre de Absalon Opazo (Atencion al Cliente,
 *         EntelPcs)
 * 
 */
public class ResultadoServicioBean implements Serializable {

    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    private String numeroPcs;

    private String correlativoTransferencia;

    public String getNumeroPcs() {
        return numeroPcs;
    }

    public void setNumeroPcs(String numeroPcs) {
        this.numeroPcs = numeroPcs;
    }

    public String getCorrelativoTransferencia() {
        return correlativoTransferencia;
    }

    public void setCorrelativoTransferencia(String correlativoTransferencia) {
        this.correlativoTransferencia = correlativoTransferencia;
    }

}
