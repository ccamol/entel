package com.epcs.bean;

import java.io.Serializable;
import java.util.ArrayList;

/**
 * @author I2B, haltamiranda.
 *
 */
public class ConsultaSintomaBean implements Serializable{
	
	private static final long serialVersionUID = 1L;
	
	String codigoRespuesta;	
	String mensajeRespuesta;	
	ArrayList<SintomaBean> listaSintoma;

	/**
	 * @return the codigoRespuesta
	 */
	public String getCodigoRespuesta() {
		return codigoRespuesta;
	}

	/**
	 * @param codigoRespuesta the codigoRespuesta to set
	 */
	public void setCodigoRespuesta(String codigoRespuesta) {
		this.codigoRespuesta = codigoRespuesta;
	}

	/**
	 * @return the mensajeRespuesta
	 */
	public String getMensajeRespuesta() {
		return mensajeRespuesta;
	}

	/**
	 * @param mensajeRespuesta the mensajeRespuesta to set
	 */
	public void setMensajeRespuesta(String mensajeRespuesta) {
		this.mensajeRespuesta = mensajeRespuesta;
	}

	/**
	 * @return the listaSintoma
	 */
	public ArrayList<SintomaBean> getListaSintoma() {
		return listaSintoma;
	}

	/**
	 * @param listaSintoma the listaSintoma to set
	 */
	public void setListaSintoma(ArrayList<SintomaBean> listaSintoma) {
		this.listaSintoma = listaSintoma;
	}
	


}
