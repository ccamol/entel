package com.epcs.bean;


public class ResultadoConsultarDetalleCuenta {
	
	  
	    private String documentoGUID;
	  
	    private String cantidadPaginas;
	  
	    private String cantidadSecciones;
	  
	    private String tipoDocumento;
	  
	    private String numeroDocumento;
	  
	    private String fechaVencimiento;
	  
	    private String saldoPagar;
	    
	    

		public String getDocumentoGUID() {
			return documentoGUID;
		}

		public void setDocumentoGUID(String documentoGUID) {
			this.documentoGUID = documentoGUID;
		}

		public String getCantidadPaginas() {
			return cantidadPaginas;
		}

		public void setCantidadPaginas(String cantidadPaginas) {
			this.cantidadPaginas = cantidadPaginas;
		}

		public String getCantidadSecciones() {
			return cantidadSecciones;
		}

		public void setCantidadSecciones(String cantidadSecciones) {
			this.cantidadSecciones = cantidadSecciones;
		}

		public String getTipoDocumento() {
			return tipoDocumento;
		}

		public void setTipoDocumento(String tipoDocumento) {
			this.tipoDocumento = tipoDocumento;
		}

		public String getNumeroDocumento() {
			return numeroDocumento;
		}

		public void setNumeroDocumento(String numeroDocumento) {
			this.numeroDocumento = numeroDocumento;
		}

		public String getFechaVencimiento() {
			return fechaVencimiento;
		}

		public void setFechaVencimiento(String fechaVencimiento) {
			this.fechaVencimiento = fechaVencimiento;
		}

		public String getSaldoPagar() {
			return saldoPagar;
		}

		public void setSaldoPagar(String saldoPagar) {
			this.saldoPagar = saldoPagar;
		}
	    
	    
	    
	    

}
