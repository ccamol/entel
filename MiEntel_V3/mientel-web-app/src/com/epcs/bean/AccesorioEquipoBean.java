package com.epcs.bean;


public class AccesorioEquipoBean {

	private String codAccesorio;
    
    private String descAccesorio;
	
    public AccesorioEquipoBean(){
	   
    }

	/**
	 * @return the codAccesorio
	 */
	public String getCodAccesorio() {
		return codAccesorio;
	}

	/**
	 * @param codAccesorio the codAccesorio to set
	 */
	public void setCodAccesorio(String codAccesorio) {
		this.codAccesorio = codAccesorio;
	}

	/**
	 * @return the descAccesorio
	 */
	public String getDescAccesorio() {
		return descAccesorio;
	}

	/**
	 * @param descAccesorio the descAccesorio to set
	 */
	public void setDescAccesorio(String descAccesorio) {
		this.descAccesorio = descAccesorio;
	}
    
}
