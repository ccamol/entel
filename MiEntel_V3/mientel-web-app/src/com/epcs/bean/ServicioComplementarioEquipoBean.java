package com.epcs.bean;


public class ServicioComplementarioEquipoBean {
	
    private String descServicio;

    private String tipoMoneda;

    private double valor;

    private String observacion;

    public ServicioComplementarioEquipoBean(){
    	
    }
    
	/**
	 * @return the descServicio
	 */
	public String getDescServicio() {
		return descServicio;
	}

	/**
	 * @param descServicio the descServicio to set
	 */
	public void setDescServicio(String descServicio) {
		this.descServicio = descServicio;
	}

	/**
	 * @return the tipoMoneda
	 */
	public String getTipoMoneda() {
		return tipoMoneda;
	}

	/**
	 * @param tipoMoneda the tipoMoneda to set
	 */
	public void setTipoMoneda(String tipoMoneda) {
		this.tipoMoneda = tipoMoneda;
	}

	/**
	 * @return the valor
	 */
	public double getValor() {
		return valor;
	}

	/**
	 * @param valor the valor to set
	 */
	public void setValor(double valor) {
		this.valor = valor;
	}

	/**
	 * @return the observacion
	 */
	public String getObservacion() {
		return observacion;
	}

	/**
	 * @param observacion the observacion to set
	 */
	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}       
	
}
