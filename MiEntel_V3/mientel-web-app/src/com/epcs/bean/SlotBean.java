public class SlotBean implements Serializable {
	
	 private static final long serialVersionUID = 1L;
	 
	 private int idSlot;
	 
	 private String nombreSlot;
	 
	 private String usoSlot;
	 
	 private String url;
	 
	 private Double costoAgregar;
	 
	 private Double costoEditar;
	 
	 private Double costoBorrar;
	 
	 

	 /**
	  * 
	  * @return
	  */
	public int getIdSlot() {
		return idSlot;
	}

	/**
	 * 
	 * @param idSlot
	 */
	public void setIdSlot(int idSlot) {
		this.idSlot = idSlot;
	}

	/**
	 * 
	 * @return
	 */
	public String getNombreSlot() {
		return nombreSlot;
	}

	/**
	 * 
	 * @param nombreSlot
	 */
	public void setNombreSlot(String nombreSlot) {
		this.nombreSlot = nombreSlot;
	}

	/**
	 * 
	 * @return
	 */
	public String getUsoSlot() {
		return usoSlot;
	}

	/**
	 * 
	 * @param usoSlot
	 */
	public void setUsoSlot(String usoSlot) {
		this.usoSlot = usoSlot;
	}

	/**
	 * 
	 * @return
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * 
	 * @param url
	 */
	public void setUrl(String url) {
		this.url = url;
	}

	/**
	 * 
	 * @return
	 */
	public Double getCostoAgregar() {
		return costoAgregar;
	}

	/**
	 * 
	 * @param costoAgregar
	 */
	public void setCostoAgregar(Double costoAgregar) {
		this.costoAgregar = costoAgregar;
	}

	/**
	 * 
	 * @return
	 */
	public Double getCostoEditar() {
		return costoEditar;
	}

	/**
	 * 
	 * @param costoEditar
	 */
	public void setCostoEditar(Double costoEditar) {
		this.costoEditar = costoEditar;
	}

	/**
	 * 
	 * @return
	 */
	public Double getCostoBorrar() {
		return costoBorrar;
	}

	/**
	 * 
	 * @param costoBorrar
	 */
	public void setCostoBorrar(Double costoBorrar) {
		this.costoBorrar = costoBorrar;
	}
	
	
	
	/**
	 * 
	 * @return id slot numero frecuente
	 */
	public int getSlotNumeroFrecuente() {
		return PlanHelper.getSlotNumeroFrecuente();
	}


	/**
	 * 
	 * @return id slot numero favorito
	 */
	public int getSlotNumeroFavorito() {
		return PlanHelper.getSlotNumeroFavorito();
	}
	
	
	
	
}
